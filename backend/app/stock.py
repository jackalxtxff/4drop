"""Расчёт остатка, публикуемого на маркетплейсе.

Отдельный модуль, потому что этой логикой пользуются два места: список товаров
(показать пользователю, сколько уйдёт на площадку) и будущий пуш остатков. Держать
формулу в одном месте — чтобы витрина и то, что реально отправляется, не разошлись.
"""

from __future__ import annotations


def marketplace_stock(real_stock: int, buffer: int) -> int:
    """Остаток для маркетплейса = реальный минус буфер, но не ниже нуля.

    Буфер страхует от оверселла на последних штуках: пока реального остатка не
    больше буфера, на площадку уходит 0 — товар просто не продаётся, вместо того
    чтобы быть проданным и не отгруженным.

    >>> marketplace_stock(8, 2)
    6
    >>> marketplace_stock(2, 2)
    0
    >>> marketplace_stock(1, 2)
    0
    >>> marketplace_stock(8, 0)
    8
    """
    if buffer <= 0:
        return max(real_stock, 0)
    return max(real_stock - buffer, 0)
